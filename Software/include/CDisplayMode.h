#ifndef CDISPLAYMODE_H_
#define CDISPLAYMODE_H_

//////////////////////////////////////////////////////////////////////////////

namespace RGBLightControl {

  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////
  //! This enumeration holds the values for the different display modes.
  //////////////////////////////////////////////////////////////////////////

  typedef enum { DISPLAYMODE_CONSTANT = 0x02 } t_DisplayMode;


  //////////////////////////////////////////////////////////////////////////
  //! This wrapper encapsulates the mode of the RGBLightControl system for
  //! convenience purposes.
  //////////////////////////////////////////////////////////////////////////

  class CDisplayMode
  {
  public:
    CDisplayMode(t_DisplayMode eMode = DISPLAYMODE_CONSTANT);
    virtual ~CDisplayMode();

    CDisplayMode & operator()(t_DisplayMode eMode);
    char operator()() const;

  protected:
    t_DisplayMode m_eMode;
  };


  //////////////////////////////////////////////////////////////////////////////

} // namespace RGBLightControl

//////////////////////////////////////////////////////////////////////////////
#endif /* CDISPLAYMODE_H_ */
