#include "CBaseConfiguration.h"

//////////////////////////////////////////////////////////////////////////////

namespace RGBLightControl {

  //////////////////////////////////////////////////////////////////////////////


  //////////////////////////////////////////////////////////////////////////
  //! Construction
  //////////////////////////////////////////////////////////////////////////

  CBaseConfiguration::CBaseConfiguration()
  {
  }


  //////////////////////////////////////////////////////////////////////////
  //! Destruction
  //////////////////////////////////////////////////////////////////////////

  CBaseConfiguration::~CBaseConfiguration()
  {
  }


  //////////////////////////////////////////////////////////////////////////
  //! This method returns an iterator to the configuration of the first
  //! LED module.
  //////////////////////////////////////////////////////////////////////////

  CBaseConfiguration::iterator CBaseConfiguration::begin()
  {
    return CBaseConfiguration::iterator(&m_aoConfig[0]);
  }


  //////////////////////////////////////////////////////////////////////////
  //! This method returns an iterator past the configuration of the
  //! last LED module.
  //////////////////////////////////////////////////////////////////////////

  CBaseConfiguration::iterator CBaseConfiguration::end()
  {
    return CBaseConfiguration::iterator(&m_aoConfig[NUMBER_OF_LEDS]);
  }


  //////////////////////////////////////////////////////////////////////////
  //! This method returns a constant iterator to the configuration of the
  //! first LED module.
  //////////////////////////////////////////////////////////////////////////

  CBaseConfiguration::const_iterator CBaseConfiguration::begin() const
  {
    return CBaseConfiguration::const_iterator(m_aoConfig);
  }


  //////////////////////////////////////////////////////////////////////////
  //! This method returns a constant iterator past the configuration of
  //! the last LED module.
  //////////////////////////////////////////////////////////////////////////

  CBaseConfiguration::const_iterator CBaseConfiguration::end() const
  {
    return CBaseConfiguration::const_iterator(m_aoConfig + NUMBER_OF_LEDS);
  }



  //////////////////////////////////////////////////////////////////////////////

} // namespace RGBLightControl

