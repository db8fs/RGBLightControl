#include "CBaseMessage.h"

//////////////////////////////////////////////////////////////////////////////

namespace RGBLightControl {

  //////////////////////////////////////////////////////////////////////////////


  //////////////////////////////////////////////////////////////////////////
  //! Construction
  //////////////////////////////////////////////////////////////////////////

  CBaseMessage::CBaseMessage()
  {
  }


  //////////////////////////////////////////////////////////////////////////
  //! Destruction
  //////////////////////////////////////////////////////////////////////////

  CBaseMessage::~CBaseMessage()
  {
  }


  //////////////////////////////////////////////////////////////////////////
  //! This method returns an iterator to the first character of the message.
  //////////////////////////////////////////////////////////////////////////

  CBaseMessage::iterator CBaseMessage::begin()
  {
    return CBaseMessage::iterator(&m_acMessage[0]);
  }


  //////////////////////////////////////////////////////////////////////////
  //! This method returns an iterator past the last character of the message.
  //////////////////////////////////////////////////////////////////////////

  CBaseMessage::iterator CBaseMessage::end()
  {
    return CBaseMessage::iterator(&m_acMessage[MESSAGE_SIZE]);
  }


  //////////////////////////////////////////////////////////////////////////
  //! This method returns an iterator to the first character of the message.
  //////////////////////////////////////////////////////////////////////////

  CBaseMessage::const_iterator CBaseMessage::begin() const
  {
    return CBaseMessage::const_iterator(m_acMessage);
  }


  //////////////////////////////////////////////////////////////////////////
  //! This method returns an iterator past the last character of the message.
  //////////////////////////////////////////////////////////////////////////

  CBaseMessage::const_iterator CBaseMessage::end() const
  {
    return CBaseMessage::const_iterator(m_acMessage + MESSAGE_SIZE);
  }




  //////////////////////////////////////////////////////////////////////////////

} // namespace RGBLightControl

